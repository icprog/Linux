/***************************************************************************
输出结果：
m=1,n=1
***************************************************************************/
#include<stdio.h>

int main(void)
{
	int i=7, j=-7, k=-8;
	int m,n;
	unsigned int a=i;
	m=(a+j==0)?1:0;
	n=(a+k>0)?1:0;
	printf("m=%d,n=%d\n",m,n);
	return 0;
}
