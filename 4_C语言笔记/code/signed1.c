/***************************************************************************
输出结果：
a+b>0
z<0
***************************************************************************/
#include<stdio.h>

int main()
{
	int a = -6;
	unsigned int b = 4;
	if(a+b > 0)
		printf("a+b>0\n");//这句话被打印
	else
		printf("a+b<0\n");
	int z = a+b;
	if(z > 0)
		printf("z>0\n");
	else
		printf("z<0\n");//这句话被打印

	return 0;
}
