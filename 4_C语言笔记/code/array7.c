/***************************************************************************
输出结果：
1 2 3 4 5
i=1,j=2,k=3
1 3 3 4 5
***************************************************************************/
#include<stdio.h>

int main(void)
{
	char i,j,k;
	char a[5]={1,2,3,4,5};
	char *p=a;
	printf("%d %d %d %d %d\n",a[0],a[1],a[2],a[3],a[4]);

	i=*p++;//1
	j=(*p)++;//2
	k=*(p++);//3
	printf("i=%d,j=%d,k=%d\n",i,j,k);
	printf("%d %d %d %d %d\n",a[0],a[1],a[2],a[3],a[4]);
}
